    public void initData() 
    {
        DefaultTableModel model = (DefaultTableModel) table2.getModel();
        
        String SQL = "SELECT * FROM to_do;";
        
        String status = null;
        
        int count = 0;
        
        Connection connection = Connector.connection();
        
        table_clean();
        
        try
        {
            Statement stmt = connection.createStatement();
            
            ResultSet rs = stmt.executeQuery(SQL);
            
            while(rs.next())
            {
                String date = rs.getString("task_date");
                
                String acid =  rs.getString("Activity_ID");
                
                String activity =  rs.getString("activity");
                
                status = "Pending";
                
                count++;
                
                model.addRow(new ModelStaff(new ImageIcon(getClass().getResource("/img/cloud_development_100px.png")), activity, date, acid, status).toDataTable());
            }
            
            connection.close();
        }
        catch(SQLException ERROR)
        {
            Bugs_Log.exceptions(SQL);
            
            System.out.println(ERROR);
        }

        table2.fixTable(jScrollPane2);
        
        table2.setFont(new java.awt.Font("Yu Gothic UI", 0, 14));
    }
    
    public String local_date()
    {
        Date get_date = date.getDate();
         
        DateFormat date_format = new SimpleDateFormat("yyyy-MM-dd");
        
        Date newdate = get_date;
        
        return String.valueOf(date_format.format(newdate));
    }

